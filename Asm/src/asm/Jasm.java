    public void filltotable() {
        DefaultTableModel tb = (DefaultTableModel) tblnv.getModel();
        tb.setRowCount(0);//xóa trắng hàng table//
        //Khai báo biến//
        String code, name, email, sex, pic;
        double salary;
        int age;
        /////
        for (int i = 0; i < list.size(); i++) {
            Asm h1 = list.get(i);
            name = h1.getName();
            code = h1.getCode();
            email = h1.getEmail();
            sex = h1.getSex();
            salary = h1.getSalary();
            pic = h1.getPic();
            age = h1.getAge();
            Object[] row = new Object[]{code, name, age, email, salary, sex, pic};
            tb.addRow(row);
        }
        
    }
    
