    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        ////Đưa vào table////  
        Asm nv = new Asm();
        int code = txtcode.getText().length();
        int name = txtname.getText().length();
        int email = txtemail.getText().length();
        int age = txtage.getText().length();
        int salary = txtsalary.getText().length();
        if (code == 0) {
            JOptionPane.showMessageDialog(null, "Mã nhân viên không để trống");
        } else if (name == 0) {
            JOptionPane.showMessageDialog(null, "Họ tên không để trống");
        } else if (email == 0) {
            JOptionPane.showMessageDialog(null, "Email không được để trống");
        } else if (age == 0) {
            JOptionPane.showMessageDialog(null, "Tuổi không để trống");
        } else if (salary == 0) {
            JOptionPane.showMessageDialog(null, "Lương không để trống");
        } else {
            try {
                nv.setAge(Integer.parseInt(txtage.getText()));
                nv.setSalary(Double.parseDouble(txtsalary.getText()));
            } catch (Error404 ex) {
                JOptionPane.showMessageDialog(this, ex.error);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Dữ liệu số bị sai ");
            }
        }
        if (checkxxx() == true) {
            JOptionPane.showMessageDialog(null, "Bạn đã nhập cùng mã số" + txtcode.getText());
            
        } else {
            addlist();
            filltotable();
            JOptionPane.showMessageDialog(null, "Đã thêm thành công");
        }
        

    }//GEN-LAST:event_btnaddActionPerformed

